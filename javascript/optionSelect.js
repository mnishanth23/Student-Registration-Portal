
function GetSelectedTextValue(selsectOptions) {
    var selectedText = selsectOptions.options[selsectOptions.selectedIndex].innerHTML;
    // var selectedText = selsectOptions.options[selsectOptions].innerHTML;
    var selectedValue = selsectOptions.value;
    // alert("Selected Text: " + selectedText + " Value: " + selectedValue);
    if (selectedValue == "All Clear") {
        document.getElementById('Input').focus();
        // document.getElementById("Input").value = "";
        document.getElementById('Input').readOnly = false;
        
    } else {
        document.getElementById("Input").value = selectedValue;
        document.getElementById('Input').readOnly = true;
    }
}

